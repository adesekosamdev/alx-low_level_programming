/**
 * op_add - returns sum of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: the result
 */

int op_add(int a, int b)
{
	int result;

	result = a + b;
	return (result);
}

/**
 * op_sub - returns difference of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: the result
 */

int op_sub(int a, int b)
{
	int result;

	result = a - b;
	return (result);
}

/**
 * op_mul - returns product of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: the result
 */

int op_mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}

/**
 * op_div - returns quotient of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: the result
 */

int op_div(int a, int b)
{
	int result;

	result = a / b;
	return (result);
}

/**
 * op_mod - returns the remainder from division of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: the result
 */

int op_mod(int a, int b)
{
	int result;

	result = a % b;
	return (result);
}
